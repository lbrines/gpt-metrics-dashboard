#!/bin/bash

# Exit on error
set -e

# Colors for output
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
NC='\033[0m' # No Color

echo -e "${GREEN}=== Configuring Nginx for Docker ===${NC}"

# Get the project root directory
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PROJECT_ROOT="$(dirname "$(dirname "$SCRIPT_DIR")")"

# Create nginx directory if it doesn't exist
NGINX_DIR="$PROJECT_ROOT/nginx"
mkdir -p "$NGINX_DIR"

echo -e "${YELLOW}Creating Nginx configuration for Docker container...${NC}"

# Create main nginx.conf if it doesn't exist
if [ ! -f "$NGINX_DIR/nginx.conf" ]; then
    cat > "$NGINX_DIR/nginx.conf" <<EOL
user nginx;
worker_processes auto;
pid /var/run/nginx.pid;

events {
    worker_connections 1024;
    multi_accept on;
    use epoll;
}

http {
    # Basic Settings
    sendfile on;
    tcp_nopush on;
    tcp_nodelay on;
    keepalive_timeout 65;
    types_hash_max_size 2048;
    server_tokens off;
    client_max_body_size 100M;

    # MIME Types
    include /etc/nginx/mime.types;
    default_type application/octet-stream;

    # Logging Settings
    access_log /var/log/nginx/access.log;
    error_log /var/log/nginx/error.log;

    # Gzip Settings
    gzip on;
    gzip_disable "msie6";
    gzip_vary on;
    gzip_proxied any;
    gzip_comp_level 6;
    gzip_buffers 16 8k;
    gzip_http_version 1.1;
    gzip_types text/plain text/css application/json application/javascript text/xml application/xml application/xml+rss text/javascript;

    # Include server configurations
    include /etc/nginx/conf.d/*.conf;
}
EOL
    echo -e "${GREEN}Created nginx.conf${NC}"
fi

# Create default server configuration if it doesn't exist
if [ ! -f "$NGINX_DIR/default.conf" ]; then
    cat > "$NGINX_DIR/default.conf" <<EOL
server {
    listen 80;
    server_name localhost;

    # Frontend
    location / {
        proxy_pass http://frontend:3000;
        proxy_http_version 1.1;
        proxy_set_header Upgrade \$http_upgrade;
        proxy_set_header Connection 'upgrade';
        proxy_set_header Host \$host;
        proxy_cache_bypass \$http_upgrade;
        proxy_set_header X-Real-IP \$remote_addr;
        proxy_set_header X-Forwarded-For \$proxy_add_x_forwarded_for;
        proxy_set_header X-Forwarded-Proto \$scheme;
    }

    # Backend API
    location /api {
        proxy_pass http://backend:8000;
        proxy_http_version 1.1;
        proxy_set_header Upgrade \$http_upgrade;
        proxy_set_header Connection 'upgrade';
        proxy_set_header Host \$host;
        proxy_cache_bypass \$http_upgrade;
        proxy_set_header X-Real-IP \$remote_addr;
        proxy_set_header X-Forwarded-For \$proxy_add_x_forwarded_for;
        proxy_set_header X-Forwarded-Proto \$scheme;
    }

    # Health check
    location /health {
        access_log off;
        return 200 "healthy\n";
        add_header Content-Type text/plain;
    }
}
EOL
    echo -e "${GREEN}Created default.conf${NC}"
fi

# Set proper permissions
chmod 644 "$NGINX_DIR/nginx.conf"
chmod 644 "$NGINX_DIR/default.conf"

echo -e "${GREEN}=== Nginx configuration for Docker completed successfully ===${NC}"
echo -e "${YELLOW}Configuration files created in: $NGINX_DIR${NC}"
echo -e "${YELLOW}These files will be mounted into the Nginx Docker container${NC}"
